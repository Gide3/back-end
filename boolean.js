// Operadores de Comparação
// Operador de valor (compara apenas o valor)
console.log("1" == 1)
console.log(true == 1)
console.log(false == 0)
console.log(false == "")

// Operadores de igualdade estrita
// (compara valor e tipo)
console.log("1" === 1)
console.log(true === 1)
console.log(false === 0)

// Verificar tipos de variável
console.log(typeof("1")) // -> texto (string)
console.log(typeof(1)) // -> number
console.log(typeof (true)) //-> boolean